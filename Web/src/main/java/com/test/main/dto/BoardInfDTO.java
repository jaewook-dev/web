package com.test.main.dto;

public class BoardInfDTO {

	// DTO 변수 선언
	private String sBoardNO;
	private String sBoardName;
	private String sBoardServer;
	private String sBoardMaster;
	private String sBoardETC;
	
	public String getsBoardNO() {
		return sBoardNO;
	}
	public void setsBoardNO(String sBoardNO) {
		this.sBoardNO = sBoardNO;
	}
	public String getsBoardName() {
		return sBoardName;
	}
	public void setsBoardName(String sBoardName) {
		this.sBoardName = sBoardName;
	}
	public String getsBoardServer() {
		return sBoardServer;
	}
	public void setsBoardServer(String sBoardServer) {
		this.sBoardServer = sBoardServer;
	}
	public String getsBoardMaster() {
		return sBoardMaster;
	}
	public void setsBoardMaster(String sBoardMaster) {
		this.sBoardMaster = sBoardMaster;
	}
	public String getsBoardETC() {
		return sBoardETC;
	}
	public void setsBoardETC(String sBoardETC) {
		this.sBoardETC = sBoardETC;
	}
}
